    private void btnIzmeniVozActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniVozActionPerformed
        String brojSedista= jTxtBrojSedista.getText();
        int brojSedistaInt = 0;
        if(brojSedista.length() == 0){
            JOptionPane.showMessageDialog(this, "Morate uneti broj sedista voza!");
            return;
        }
        try {
            brojSedistaInt = Integer.parseInt(brojSedista);
            Voz voz = (Voz) cbVozovi.getSelectedItem();
            voz.setBrojSedista(brojSedistaInt);
            Communication.getInstance().zapamtiVoz(voz);
            JOptionPane.showMessageDialog(this, "Uspenso izmenjen broj sedista voza");
            cbVozovi.removeAllItems();
            prepareIzmena();
        } catch (NumberFormatException e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Broj sedista voza mora biti broj");
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Greska pri izmeni broja sedista voza!","Greska",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniVozActionPerformed

    private void cbVozoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbVozoviActionPerformed
        // TODO add your handling code here:
        Voz voz = (Voz)cbVozovi.getSelectedItem();
        if(voz != null){
            jTxtBrojSedista.setEnabled(true);
            jTxtVozID.setText(String.valueOf(voz.getVozID()));
            jTxtNaziv.setText(voz.getNazivVoza());
            jTxtBrojSedista.setText(String.valueOf(voz.getBrojSedista()));
            jTxtDatumProizvodnje.setText(voz.getGodinaProizvodnje()+"");
            jTxtVrsta.setText(voz.getVrstaVozaID().toString());
        }
    }//GEN-LAST:event_cbVozoviActionPerformed

