    private void prepareIzmena()  {
        try {
            List<Voz> vozovi = Communication.getInstance().ucitajListuVozova();
   
            for (Voz voz : vozovi) {
                cbVozovi.addItem(voz);
            }
            cbVozovi.setEnabled(true);
        } catch (Exception ex) {
            Logger.getLogger(FrmIzmeniVoz.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
